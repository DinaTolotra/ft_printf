#include "ft_printf.h"

static long long	ft_abs(long long lnb)
{
	if (lnb >= 0)
		return (lnb);
	return (-lnb);
}

int	ft_putlnb_rec(long lnb)
{
	int	count;

	count = 1;
	if (lnb >= 10)
		count += ft_putlnb_rec(lnb / 10);
	ft_putchar((lnb % 10) + '0');
	return (count);
}

int	ft_putlnb(long nb)
{
	long long	lnb;
	int			count;

	count = 0;
	lnb = ft_abs(nb);
	if (nb < 0)
	{
		ft_putchar('-');
		count++;
	}
	return (count + ft_putlnb_rec(lnb));
}
